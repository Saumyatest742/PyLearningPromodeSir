# Explain the difference between = operator and == operator
# = operator is assignment operator, it assigns the value on the right side to the variable on the left side

a = 5 # Assigning the value 5 to the variable a

# == operator is a comparison operator, it checks if the value on the left side is equal to the value on the right side
if a == 5: # Checking if a is equal to 5
    print("a is equal to 5")

# What is ^ operator in Python?
# The ^ operator is a bitwise operator
x = 10# Assigning the value 10 to the variable x
x = x^3
print(x) # Printing the value of x after performing the bitwise XOR operation

